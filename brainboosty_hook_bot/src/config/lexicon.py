def recommendation_for_goals(goal_keys: list[str]) -> str:
    """Выбирает текст рекомендации по списку целей (ключи как в brain_map)."""
    if not goal_keys:
        return RECOMMENDATIONS["all"]
    if "all" in goal_keys:
        return RECOMMENDATIONS["all"]
    first = goal_keys[0]
    return RECOMMENDATIONS.get(first, RECOMMENDATIONS["all"])


