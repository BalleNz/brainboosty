def cognitive_question_text(variant: str, q_index_1_based: int) -> str:
    """Текст вопроса 1..7 для стилистики sexual | development."""
    if variant == "sexual":
        body = COGNITIVE_QUESTION_BODIES_SEXUAL[q_index_1_based - 1]
    else:
        body = COGNITIVE_QUESTION_BODIES_DEVELOPMENT[q_index_1_based - 1]
    return f"Вопрос {q_index_1_based}/{COGNITIVE_NUM_QUESTIONS}\n\n{body}"


def brain_map_block_title_for_variant(variant: str) -> str:
    if variant == "sexual":
        return BRAIN_MAP_TITLE_SEXUAL_BLOCK
    return BRAIN_MAP_TITLE_DEVELOPMENT_BLOCK


