def question_body(lang: str, variant: str, index_1_based: int) -> str:
    lang = "en" if lang == "en" else "ru"
    i = index_1_based - 1
    if variant == "sexual":
        lst = EN_SEXUAL if lang == "en" else RU_SEXUAL
    else:
        lst = EN_DEV if lang == "en" else RU_DEV
    return lst[i]


def question_header(lang: str, n: int, total: int) -> str:
    if lang == "en":
        return f"Question {n}/{total}\n\n"
    return f"Вопрос {n}/{total}\n\n"


def format_question(lang: str, variant: str, index_1_based: int, total: int = 7) -> str:
    return question_header(lang, index_1_based, total) + question_body(lang, variant, index_1_based)
